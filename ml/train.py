# =============================================================================
# EnerSense — ml/train.py
# Trains a Ridge regression model to predict next-hour energy consumption.
#
# Features:
#   - hour_sin, hour_cos   : cyclic encoding of hour of day
#   - day_of_week          : 0 (Monday) – 6 (Sunday)
#   - lag_1h, lag_2h, lag_3h : energy consumed in the previous 1/2/3 hours
#
# Target:
#   - energy_next_1h (kWh) : energy that will be consumed in the next hour
#
# Usage:
#   python ml/train.py                        # pulls data from InfluxDB
#   python ml/train.py --csv data/sample.csv  # use a CSV file instead
#
# Output:
#   ml/model.joblib      trained model
#   ml/scaler.joblib     fitted StandardScaler
#   ml/metrics.json      MAE, RMSE, R² on validation set
# =============================================================================

import argparse
import json
import math
import os
import sys
import warnings

import numpy as np
import pandas as pd
import joblib

from sklearn.linear_model    import Ridge
from sklearn.preprocessing   import StandardScaler
from sklearn.pipeline        import Pipeline
from sklearn.metrics         import mean_absolute_error, mean_squared_error, r2_score
from sklearn.model_selection import TimeSeriesSplit

warnings.filterwarnings('ignore')

